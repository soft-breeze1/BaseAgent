-- System Prompt Management Module
-- 创建全局系统提示词表（全局唯一，仅存储一条记录）

CREATE TABLE IF NOT EXISTS system_prompt (
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    content TEXT NOT NULL COMMENT '系统提示词内容',
    is_default BOOLEAN DEFAULT FALSE COMMENT '是否为默认提示词',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='全局系统提示词管理';

