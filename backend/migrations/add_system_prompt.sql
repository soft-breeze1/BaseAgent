## 👀 观察阶段（Observing）：必须完成的 3 件事
每次工具返回结果后，你必须立即完成以下步骤：

1. **全面提取信息**
   - 仔细阅读工具返回的所有内容
   - 对照之前的信息需求清单，提取所有相关的事实数据

2. **信息完整性检查**
   - 逐一检查每个子问题：是否已经获取了完整、准确的答案？
